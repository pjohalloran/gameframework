#ifndef ___GF_GLFW_GAME_TIMER_H
#define ___GF_GLFW_GAME_TIMER_H

// External Headers
#include "GameBase.h"


// Project Headers
#include "CommonTimer.h"

namespace GameHalloran
{
	// /////////////////////////////////////////////////////////////////
	// @class GlfwGameTimer
	// @author PJ O Halloran
	//
	// This class has methods for stopping, starting and querying a timer
	// using the GLFW library.
	//
	// /////////////////////////////////////////////////////////////////
	class GlfwGameTimer : public CommonTimer
	{
	private:
		
		// /////////////////////////////////////////////////////////////////
		// Get the current tick count from the underlying OS in a platform 
		// specific way.
		//
		// /////////////////////////////////////////////////////////////////
		virtual F64 VGetTicks()
		{
			return (glfwGetTime());	
		};

	public:
	
		// /////////////////////////////////////////////////////////////////
		// Default constructor.
		//
		// /////////////////////////////////////////////////////////////////
		GlfwGameTimer() : CommonTimer() { };
		
		// /////////////////////////////////////////////////////////////////
		// Destructor.
		//
		// /////////////////////////////////////////////////////////////////
		virtual ~GlfwGameTimer() { };
			
	};
}

#endif
