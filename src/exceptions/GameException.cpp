#include "GameException.h"

using std::exception;

namespace GameHalloran {
    // /////////////////////////////////////////////////////////////////
    //
    // /////////////////////////////////////////////////////////////////
    GameException::GameException() : exception()
    {
    }

    // /////////////////////////////////////////////////////////////////
    //
    // /////////////////////////////////////////////////////////////////
    GameException::GameException(const std::string &errorMessageRef) \
:
    m_msg(errorMessageRef)
    {
    }

    // /////////////////////////////////////////////////////////////////
    //
    // /////////////////////////////////////////////////////////////////
    GameException::~GameException() throw()
    {
    }

    // /////////////////////////////////////////////////////////////////
    //
    // /////////////////////////////////////////////////////////////////
    const char *GameException::what() const throw()
    {
        return (m_msg.c_str());
    }
}
